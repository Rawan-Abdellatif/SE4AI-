const fs = require("fs");

// Function to read an image file and convert it to a buffer
function imageToBuffer(filePath) {
  try {
    const image = fs.readFileSync(filePath);
    return Buffer.from(image);
  } catch (error) {
    console.error("Error reading image:", error);
    return null;
  }
}

